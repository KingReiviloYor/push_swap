#include "../libft.h"

int	ft_isint(char *str)
{
	if (!str)
		return (0);
	if (*str == '+' || *str == '-')
		str++;
	while (*str)
	{
		if (!ft_isdigit(*str))
			return (0);
		str++;
	}
	if (!ft_isdigit(*(str - 1)))
		return (0);
	return (1);
}
